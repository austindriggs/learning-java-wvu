/*
 * Object Oriented Programming
 * ADT means Abstract Data Type
 * 
 * 
 * 
 * 
 * 
 * If we were designing a class Dog
 * We would need: breed, color, gender, coat type, etc.
 * 
 * If we were designing a class Bakery
 * We would need: cookie, cake, bread, etc.
 * And for each: flavor, size, ingredients, instructions, etc.
 * 
 * 
 * 
 * 
 * 
 * A class can contain abstract data types for these applications: They are multivalued
 * that collect abstracts and features of an element to tell what it is. This is a way of
 * allowing the designer to create their own data type, and then as many instances of this
 * data type that they need. 
 * 
 * Ex) StudentID could be an abstract data type and each student has an instance of one.
 * FirstName, LastName, Rank, Major, SecondMajor, etc. 
 * 
 * Operations on these abstact data types are provided as sets of methods inside of the class.
 * A user can see these operations, and how they work, on an interface such as the Java API
 * However, the user doesn't really ever know what the code, or the implementation, looks like.
 * 
 * Encapsulation: a concept that all program text that implements an ADT should be contained 
 * in a single compilation unit, or file.
 * 
 * Information Hiding: the concept of separating how an item is used from its implementation. The 
 * internal information of a class should be private, and unable to be changed by the user. A user can
 * only use the methods provided.
 * 
 * 
 * 
 * 
 * Important Definitions:
 * 
 * Class: the definition of an abstract thing. It contains the declaration of attributes as variables, and
 * the implementation of methods that manipulate those attributes. It does not contain data (except if we initialize)
 * 
 * Object or Instance: One representaion of a class with distinct values for the data members. These occupy memory.
 * 
 * Method: an operation defined by the class that manipulates the attributes of the class. 
 * 
 * Message: a request to execute a method on an object (Ex: String name = "Austin"; name.length() is a message and returns 6
 * 
 * Specification: a public interface for a class. The set of methods the class makes available to create and manipulate objects.
 *  
 *  
 *  
 *  
 *  
 *  Creating a class is an iterative process
 *  
 *  SEE THE DESIGN OF A CLASS TO REPRESENT FRACTIONS
 *  
 */

